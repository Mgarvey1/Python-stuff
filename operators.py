#assignment operator
x=6
y=10
z=54.57
print(x==y==z) #equals
print(x>y>z) #greater
print(x>=y) #greater or equals to
print(x<=y) #less than or equals to
print(x<y) #less than
print(x !=y)

#logical operators
print(x>y and x==6)
print(x>y or x==6)
